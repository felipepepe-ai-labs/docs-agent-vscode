  "summary":     string,   // what it does and WHY — not just what
  "params":      [{ "name": string, "type": string, "description": string }],  // omit if none
  "returns":     string,   // omit if void
  "throws":      [string], // exception class names + when thrown — omit if none
  "sideEffects": [string]  // DB writes, state transitions, cross-service calls — omit if none
