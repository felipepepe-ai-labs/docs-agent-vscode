export function validateAndParse(raw: string): DocResult {
  const valid: SymbolDoc[] = [];
  const rejected: { raw: unknown; reason: string }[] = [];

  let parsed: unknown;
  try {
    // Strip accidental markdown fences the model may emit
    const cleaned = raw.replace(/^```[a-z]*\n?/gm, '').replace(/^```$/gm, '').trim();
    parsed = JSON.parse(cleaned);
  } catch {
    return {
      valid: [],
      rejected: [{ raw, reason: 'Response is not valid JSON' }],
    };
  }

  if (!Array.isArray(parsed)) {
    return { valid: [], rejected: [{ raw: parsed, reason: 'Root value is not an array' }] };
  }

  for (const entry of parsed) {
    const reason = validateEntry(entry);
    if (reason) {
      rejected.push({ raw: entry, reason });
    } else {
      valid.push(entry as SymbolDoc);
    }
  }

  return { valid, rejected };
}

function validateEntry(entry: unknown): string | null {
  if (typeof entry !== 'object' || entry === null) return 'Not an object';
  const e = entry as Record<string, unknown>;

  if (!e.symbol || typeof e.symbol !== 'string') return 'Missing or invalid "symbol"';
  if (!e.file || typeof e.file !== 'string') return 'Missing "file" — citation required';
  if (typeof e.line !== 'number' || e.line < 1) return 'Missing or invalid "line" — citation required';
  if (!e.summary || typeof e.summary !== 'string') return 'Missing "summary"';
  if (!['class', 'method', 'field', 'constructor'].includes(e.type as string)) return 'Invalid "type"';

  return null;
}

import * as path from 'path';
import type { ImpactSummary } from './graph';

export type ImpactLookup = (symbol: string) => ImpactSummary | null;

export function renderMarkdown(result: DocResult, sourceFile: string, lookupImpact?: ImpactLookup): string {
  const fileName = path.basename(sourceFile);
  const lines: string[] = [
    `# Documentation: ${fileName}`,
    '',
    `> Generated by Docs Agent. Every entry is grounded — file path and line number verified against source.`,
    '',
  ];

  if (result.rejected.length > 0) {
    lines.push(`## ⚠️ Rejected entries (${result.rejected.length} — missing citations)`);
    lines.push('');
    for (const r of result.rejected) {
      const sym = (r.raw as Record<string, unknown>)?.symbol ?? 'unknown';
      lines.push(`- **${sym}**: ${r.reason}`);
    }
    lines.push('');
  }

  const classes = result.valid.filter(s => s.type === 'class');
  const others = result.valid.filter(s => s.type !== 'class');

  for (const cls of classes) {
    lines.push(`## ${cls.symbol}`);
    lines.push(`*${cls.file}:${cls.line}*`);
    lines.push('');
    lines.push(cls.summary);
    lines.push('');
  }

  for (const sym of others) {
    lines.push(`### \`${sym.symbol}\``);
    lines.push(`*${sym.file}:${sym.line}*`);
    lines.push('');
    lines.push(sym.summary);

    if (sym.params?.length) {
      lines.push('');
      lines.push('**Parameters**');
      for (const p of sym.params) {
        lines.push(`- \`${p.name}\` *(${p.type})*: ${p.description}`);
      }
    }

    if (sym.returns) {
      lines.push('');
      lines.push(`**Returns**: ${sym.returns}`);
    }

    if (sym.throws?.length) {
      lines.push('');
      lines.push('**Throws**');
      for (const t of sym.throws) {
        lines.push(`- ${t}`);
      }
    }

    if (sym.sideEffects?.length) {
      lines.push('');
      lines.push('**Side effects**');
      for (const s of sym.sideEffects) {
        lines.push(`- ${s}`);
      }
    }

    if (lookupImpact) {
      const impact = lookupImpact(sym.symbol);
      if (impact) {
        const callerList = impact.callers.map(c => `\`${c.symbol}\``).join(', ');
        const tableList = [...new Set(impact.tableRefs.map(t => `\`${t.table}\` (${t.operation})`))].join(', ');
        const consumerList = impact.consumers.map(c => `\`${c.symbol}\``).join(', ');

        if (callerList || tableList || consumerList) {
          lines.push('');
          lines.push('**Impact**');
          if (callerList) lines.push(`- Called by: ${callerList}`);
          if (tableList) lines.push(`- SQL tables: ${tableList}`);
          if (consumerList) lines.push(`- Injected into: ${consumerList}`);
        }
      }
    }

    lines.push('');
  }

  return lines.join('\n');
}
